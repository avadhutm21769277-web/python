

#Arithmetic module containing Add() function, Sub() function , Mult() function and Div() function

def Add(num1,num2):
    result=0
    result=num1+num2
    return result

def Sub(num1,num2):
    result=0
    result=num1-num2
    return result

def Mult(num1,num2):
    result=0
    result=num1*num2
    return result

def Div(num1,num2):
    result=0
    result=num1/num2
    return result
